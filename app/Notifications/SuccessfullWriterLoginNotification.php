<?php

namespace App\Notifications;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;
use Illuminate\Notifications\Notification;

class SuccessfullWriterLoginNotification extends Notification
{
    use Queueable;
    public $user;

    /**
     * Create a new notification instance.
     *
     * @return void
     */
    public function __construct($user)
    {
        $this->user = $user;
    }

    /**
     * Get the notification's delivery channels.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function via($notifiable)
    {
        return ['mail'];
    }

    /**
     * Get the mail representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return \Illuminate\Notifications\Messages\MailMessage
     */
    public function toMail($notifiable)
    {
        return (new MailMessage)
            ->subject('Welcome to Scholarly Help as an Academic Writer!')
            ->greeting('Hey ' . $this->user->first_name . ' ' . $this->user->last_name . '!')
            ->line('Welcome to Scholarly Help – we’re excited to serve you!')
            ->line('You’re all ready to go!')
            ->line('Congratulations! We are glad to inform you that you have successfully joined the Scholarly Help team as a Writer. It is requested to complete your profile by accessing your dashboard.')
            ->action('Complete your Writer Profile', route('my_account'));
    }

    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        return [
            //
        ];
    }
}
